def load_good_units_from_cluster_group(derivatives_base: str) -> np.ndarray:
    """Return unit_ids labelled 'good' in Phy cluster_group.tsv at the standard path."""
    tsv = os.path.join(
        derivatives_base,
        "ephys",
        "concat_run",
        "sorting",
        "sorter_output",
        "cluster_group.tsv",
    )
    if not os.path.exists(tsv):
        raise FileNotFoundError(f"Missing cluster_group.tsv:\n{tsv}")

    df = pd.read_csv(tsv, sep="\t")
    if "KSLabel" not in df.columns or "cluster_id" not in df.columns:
        raise ValueError(f"Unexpected cluster_group.tsv columns: {list(df.columns)}")

    return df.loc[df["KSLabel"] == "good", "cluster_id"].to_numpy(dtype=int)


def load_phy_groups(derivatives_base: str) -> pd.DataFrame:
    """
    Search for Phy cluster_group.tsv under derivatives_base and return:
      unit_id, phy_group
    """
    base = Path(derivatives_base)

    hits = list(base.rglob("cluster_group.tsv"))
    if not hits:
        hits = list(base.rglob("cluster_groups.tsv"))

    if not hits:
        raise FileNotFoundError(
            "Could not find cluster_group.tsv anywhere under:\n"
            f"{base}\n\n"
            "Tip: search your derivatives folder for 'cluster_group.tsv' and update the loader."
        )

    # If multiple, pick the deepest (often the most specific sorter output)
    tsv_path = sorted(hits, key=lambda p: len(p.parts))[-1]
    print("Using cluster group file:", tsv_path)

    df = pd.read_csv(tsv_path, sep="\t")
    if "cluster_id" not in df.columns or "KSLabel" not in df.columns:
        raise ValueError(f"Unexpected columns in {tsv_path.name}: {list(df.columns)}")

    return (
        df[["cluster_id", "KSLabel"]]
        .rename(columns={"cluster_id": "unit_id", "KSLabel": "phy_group"})
        .astype({"unit_id": int})
    )


def get_sorting_and_fs(derivatives_base: str) -> Tuple[object, float]:
    """
    Loads SpikeInterface SortingAnalyzer from:
      derivatives_base/analysis/cell_characteristics/unit_features/spikeinterface_data
    """
    unit_features_path = os.path.join(
        derivatives_base, "analysis", "cell_characteristics", "unit_features"
    )
    analyzer_path = os.path.join(unit_features_path, "spikeinterface_data")
    if not os.path.exists(analyzer_path):
        raise FileNotFoundError(f"Missing sorting_analyzer:\n{analyzer_path}")

    sa = si.load_sorting_analyzer(folder=analyzer_path)
    sorting = sa.sorting

    try:
        fs_hz = float(sa.sampling_frequency)
    except Exception:
        fs_hz = float(sa.recording.get_sampling_frequency())

    return sorting, fs_hz


# ----------------------------
# ACG histogram (symmetric) for oscillation score
# ----------------------------

def acg_histogram_symmetric(
    spike_times_s: np.ndarray,
    bin_ms: float,
    max_lag_ms: float,
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Symmetric ACG histogram around 0 lag.
    Returns: lags_ms (centers), counts
    """
    st_ms = np.asarray(spike_times_s, dtype=float) * 1000.0
    st_ms = st_ms[np.isfinite(st_ms)]
    st_ms.sort()

    edges = np.arange(-max_lag_ms, max_lag_ms + bin_ms, bin_ms)
    lags_ms = edges[:-1] + bin_ms / 2.0

    if st_ms.size < 2:
        return lags_ms, np.zeros_like(lags_ms, dtype=float)

    diffs: List[float] = []
    n = st_ms.size
    for i in range(n):
        j = i + 1
        while j < n:
            d = st_ms[j] - st_ms[i]
            if d > max_lag_ms:
                break
            diffs.append(d)
            diffs.append(-d)
            j += 1

    diffs = np.asarray(diffs, dtype=float)
    counts, _ = np.histogram(diffs, bins=edges)
    return lags_ms, counts.astype(float)


# ----------------------------
# Burst metrics (positive lags only)
# ----------------------------

def burst_metrics_from_acg_positive_lags(
    spike_times_s: np.ndarray,
    bin_ms: float = 1.0,
    max_lag_ms: float = 50.0,
) -> Tuple[float, float, float, float, float]:
    """
    Returns:
      burst_index (paper) = sum(0–10)/sum(40–50)
      burst_index_norm01  = r/(1+r)  in [0,1)
      burst_contrast      = (max0_10 - mean40_50)/max0_10  (legacy/shape)
      num0_10, den40_50    (audit terms)
    """
    st_ms = np.asarray(spike_times_s, dtype=float) * 1000.0
    st_ms = st_ms[np.isfinite(st_ms)]
    st_ms.sort()

    edges = np.arange(0.0, max_lag_ms + bin_ms, bin_ms)
    bins_ms = edges[:-1] + bin_ms / 2.0

    if st_ms.size < 2:
        return np.nan, np.nan, np.nan, np.nan, np.nan

    diffs: List[float] = []
    n = st_ms.size
    for i in range(n):
        j = i + 1
        while j < n:
            d = st_ms[j] - st_ms[i]
            if d >= max_lag_ms:
                break
            diffs.append(d)
            j += 1

    diffs = np.asarray(diffs, dtype=float)
    cou, _ = np.histogram(diffs, bins=edges)
    cou = cou.astype(float)

    win_0_10 = (bins_ms >= 0.0) & (bins_ms < 10.0)
    win_40_50 = (bins_ms >= 40.0) & (bins_ms < 50.0)

    num = float(np.sum(cou[win_0_10])) if np.any(win_0_10) else np.nan
    den = float(np.sum(cou[win_40_50])) if np.any(win_40_50) else np.nan

    if np.isfinite(num) and np.isfinite(den) and den > 0:
        burst_index = float(num / den)
        burst_index_norm01 = float(burst_index / (1.0 + burst_index))
    else:
        burst_index = np.nan
        burst_index_norm01 = np.nan

    # legacy/shape "contrast"
    max0_10 = float(np.max(cou[win_0_10])) if np.any(win_0_10) else np.nan
    mean40_50 = float(np.mean(cou[win_40_50])) if np.any(win_40_50) else np.nan

    if np.isfinite(max0_10) and max0_10 != 0 and np.isfinite(mean40_50):
        temp = max0_10 - mean40_50
        if temp > 0:
            burst_contrast = float(temp / max0_10)
        elif temp < 0:
            burst_contrast = float(temp / mean40_50) if mean40_50 != 0 else np.nan
        else:
            burst_contrast = 0.0
    else:
        burst_contrast = np.nan

    return burst_index, burst_index_norm01, burst_contrast, num, den


# ----------------------------
# Oscillation score
# ----------------------------

def oscillation_score(
    spike_times_s: np.ndarray,
    bin_ms: float = 2.0,
    max_lag_ms: float = 1000.0,
    sigma_ms: float = 4.0,
    remove_ms: float = 10.0,
    fmin_hz: float = 4.0,
    fmax_hz: float = 12.0,
    min_spikes: int = 200,
) -> Tuple[float, float, float, float]:
    """
    Returns:
      oscillation_score, peak_freq_hz, M_peak, M_avg
    """
    st = np.asarray(spike_times_s, dtype=float)
    st = st[np.isfinite(st)]
    st.sort()

    if st.size < min_spikes:
        return np.nan, np.nan, np.nan, np.nan

    lags_ms, acg = acg_histogram_symmetric(st, bin_ms=bin_ms, max_lag_ms=max_lag_ms)
    acg = acg.astype(float)

    sigma_bins = sigma_ms / bin_ms
    acg_smooth = gaussian_filter1d(acg, sigma=sigma_bins)

    # remove central peak
    acg_smooth[np.abs(lags_ms) <= remove_ms] = 0.0

    # FFT
    dt_s = bin_ms / 1000.0
    spec = np.abs(rfft(acg_smooth))
    freqs = rfftfreq(acg_smooth.size, d=dt_s)

    # exclude DC
    valid = freqs > 0
    freqs = freqs[valid]
    spec = spec[valid]

    band = (freqs >= fmin_hz) & (freqs <= fmax_hz)
    if not np.any(band):
        return np.nan, np.nan, np.nan, np.nan

    M_peak = float(np.max(spec[band]))
    peak_freq = float(freqs[band][np.argmax(spec[band])])
    M_avg = float(np.mean(spec)) if spec.size > 0 else np.nan
    osc = float(M_peak / M_avg) if np.isfinite(M_avg) and M_avg > 0 else np.nan

    return osc, peak_freq, M_peak, M_avg


# ----------------------------
# CSV builder (works for good-only or all units)
# ----------------------------

def build_unit_features_df(
    unit_ids: List[int],
    sorting,
    fs_hz: float,
    df_wf: pd.DataFrame,
    df_qm: pd.DataFrame,
    df_phy: pd.DataFrame,
) -> pd.DataFrame:
    rows = []
    for uid in unit_ids:
        st_samples = sorting.get_unit_spike_train(unit_id=int(uid))
        st_s = np.asarray(st_samples, dtype=float) / fs_hz

        bi, bi01, bc, num, den = burst_metrics_from_acg_positive_lags(
            st_s, bin_ms=1.0, max_lag_ms=50.0
        )

        osc, osc_peak_f, osc_Mp, osc_Mavg = oscillation_score(
            st_s,
            bin_ms=2.0,
            max_lag_ms=1000.0,
            sigma_ms=4.0,
            remove_ms=10.0,
            fmin_hz=4.0,
            fmax_hz=12.0,
            min_spikes=200,
        )

        rows.append(
            dict(
                unit_id=int(uid),
                n_spikes=int(st_s.size),
                burst_index=bi,
                burst_index_norm01=bi01,
                burst_contrast=bc,
                acg_num_0_10=num,
                acg_den_40_50=den,
                oscillation_score=osc,
                oscillation_peak_freq_hz=osc_peak_f,
                oscillation_M_peak=osc_Mp,
                oscillation_M_avg=osc_Mavg,
            )
        )

    df_new = pd.DataFrame(rows)

    # Merge in Phy group ONCE
    df_out = (
        df_new
        .merge(df_phy, on="unit_id", how="left")
        .merge(df_wf[["unit_id", "peak_to_trough_ms"]], on="unit_id", how="left")
        .merge(df_qm[["unit_id", "firing_rate_hz"]], on="unit_id", how="left")
    )

    col_order = [
        "unit_id",
        "phy_group",
        "peak_to_trough_ms",
        "firing_rate_hz",
        "burst_index",
        "burst_index_norm01",
        "burst_contrast",
        "oscillation_score",
        "oscillation_peak_freq_hz",
        "n_spikes",
        "acg_num_0_10",
        "acg_den_40_50",
        "oscillation_M_peak",
        "oscillation_M_avg",
    ]
    return df_out[[c for c in col_order if c in df_out.columns]]


# ----------------------------
# Public entrypoint for main pipeline
# ----------------------------

def step_metrics(derivatives_base: str, out_csv_all: Path, out_csv_good: Path) -> Tuple[Path, Path]:
    """
    Compute the 4 core metrics (+ some audit columns) for:
      - ALL units
      - GOOD units (from cluster_group.tsv)

    Inputs (read-only under derivatives_base):
      - analysis/cell_characteristics/unit_features/spikeinterface_data (SortingAnalyzer)
      - analysis/cell_characteristics/unit_features/all_units_overview/unit_waveform_metrics.csv
      - analysis/cell_characteristics/unit_features/all_units_overview/unit_metrics.csv
      - ephys/concat_run/sorting/sorter_output/cluster_group.tsv

    Outputs:
      - out_csv_all
      - out_csv_good
    """
    # ---- Load precomputed waveform + firing rate from derivatives ----
    unit_features_path = os.path.join(
        derivatives_base, "analysis", "cell_characteristics", "unit_features"
    )
    wf_csv = os.path.join(unit_features_path, "all_units_overview", "unit_waveform_metrics.csv")
    qm_csv = os.path.join(unit_features_path, "all_units_overview", "unit_metrics.csv")

    if not os.path.exists(wf_csv):
        raise FileNotFoundError(f"Missing waveform metrics CSV:\n{wf_csv}")
    if not os.path.exists(qm_csv):
        raise FileNotFoundError(f"Missing unit metrics CSV:\n{qm_csv}")

    df_wf = pd.read_csv(wf_csv).rename(columns={"unit_ids": "unit_id"})
    df_qm = pd.read_csv(qm_csv).rename(columns={"unit_ids": "unit_id", "firing_rate": "firing_rate_hz"})

    if "peak_to_valley" not in df_wf.columns:
        raise ValueError(f"Expected 'peak_to_valley' in waveform CSV, got: {list(df_wf.columns)}")

    df_wf["peak_to_trough_ms"] = df_wf["peak_to_valley"].astype(float) * 1000.0

    # ---- Load sorting ----
    sorting, fs_hz = get_sorting_and_fs(derivatives_base)

    all_unit_ids = [int(u) for u in sorting.unit_ids]
    good_ids = set(map(int, load_good_units_from_cluster_group(derivatives_base)))
    good_unit_ids = [u for u in all_unit_ids if u in good_ids]

    print(f"All units:  {len(all_unit_ids)}")
    print(f"Good units: {len(good_unit_ids)}")

    df_phy = load_phy_groups(derivatives_base)

    # ---- Output paths ----
    out_csv_all = Path(out_csv_all)
    out_csv_good = Path(out_csv_good)
    out_csv_all.parent.mkdir(parents=True, exist_ok=True)
    out_csv_good.parent.mkdir(parents=True, exist_ok=True)

    # ---- Write GOOD units CSV ----
    df_good = build_unit_features_df(good_unit_ids, sorting, fs_hz, df_wf, df_qm, df_phy)
    df_good.to_csv(out_csv_good, index=False)

    # ---- Write ALL units CSV (network-safe replace) ----
    df_all = build_unit_features_df(all_unit_ids, sorting, fs_hz, df_wf, df_qm, df_phy)
    tmp_all = out_csv_all.with_suffix(".tmp.csv")
    df_all.to_csv(tmp_all, index=False)

    if out_csv_all.exists():
        try:
            out_csv_all.unlink()
        except PermissionError:
            raise PermissionError(
                f"Cannot overwrite {out_csv_all} (likely open in Excel / locked). Close it and rerun."
            )

    tmp_all.rename(out_csv_all)

    print("\nSaved:")
    print(" ", out_csv_good)
    print(" ", out_csv_all)

    return out_csv_good, out_csv_all


